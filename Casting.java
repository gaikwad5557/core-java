package day2;

public class Casting {

	public static void main(String[] args) {
		
		double  d1= 12.345;
		int i = (int)d1;   //size of double is greater than int
		System.out.println(i);
		//explicit 
		//convert double into int
		int d=34;
		double q=(double)d;
		System.out.println(d);
		System.out.println(q);
		//implicit
	}

}
