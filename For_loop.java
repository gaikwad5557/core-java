package day3;

public class For_loop {

	public static void main(String[] args) {
		for (int i = 0 ; i<5 ; i++)
		{
			System.out.println("hii");
			
		}

		
		
		//even odd
		
		for (int i=0; i<20; i++)
		{
			if (i%2==0)
			{
				System.out.println("even:"+ i);
				
			}
			else
			{
				System.out.println("no. is odd:"+i);
			}
		}
	}

}
